#ifndef PEAK_H
#define PEAK_H

#include <ngl/Vec3.h>

//-----------------------------------------------------------------------------
/// @file Peak.h
/// @author Milto Miltiadou
/// @version 1.0
/// @date 02/10/13
/// @class Peak
/// @brief this class represents a peak point of the waveform
//-----------------------------------------------------------------------------


class Peak
{
   friend class Pulse;

public:
   //-------------------------------------------------------------------------
   /// @brief defualt constructor
   //-------------------------------------------------------------------------
   Peak();
   //-------------------------------------------------------------------------
   /// @brief default copy constructor
   /// @param[in] i_peak peak point to be copied
   //-------------------------------------------------------------------------
   Peak(const Peak &i_peak);
   //-------------------------------------------------------------------------
   /// @brief default destructor
   //-------------------------------------------------------------------------
   ~Peak();

private:
   //-------------------------------------------------------------------------
   /// @brief the position of the peak point
   //-------------------------------------------------------------------------
   ngl::Vec3 m_position;
   //-------------------------------------------------------------------------
   /// @brief the itensity of the peak point
   //-------------------------------------------------------------------------
   char m_itensity;
   //-------------------------------------------------------------------------
   /// @brief the angle of the left slop
   //-------------------------------------------------------------------------
   float m_leftAngle;
   //-------------------------------------------------------------------------
   /// @brief the angle of the right slop
   //-------------------------------------------------------------------------
   float m_rightAngle;
};

#endif // PEAK_H
